/**
 * Helper to retrieve statistics for a specific region.
 * Handles potential naming mismatches or case sensitivity.
 * @param {string} regionName - The name of the region to fetch
 * @returns {Object|null} - The stats object or null if not found
 */
export const getRegionalStatistics = (regionName) => {
  if (!regionName) return null;
  // Direct lookup preferred, assuming data keys match 'locations.js' region names exactly
  return regionalStatistics[regionName] || regionalStatistics[Object.keys(regionalStatistics).find(k => k.toLowerCase() === regionName.toLowerCase())] || null;
};